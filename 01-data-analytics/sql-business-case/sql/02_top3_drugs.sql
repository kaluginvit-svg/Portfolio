-- ============================================================
-- Запрос 2: Топ-3 лекарства по объему продаж
-- ============================================================
-- Используемые конструкции: GROUP BY, SUM, ORDER BY, LIMIT
-- Логика: считаем выручку по каждому препарату (price * count),
-- группируем по drug, сортируем по убыванию, берём 3 строки.
-- ============================================================

SELECT 
    drug AS препарат,
    SUM(price * count) AS объем_продаж_руб
FROM pharma_orders
GROUP BY drug
ORDER BY объем_продаж_руб DESC
LIMIT 3;
