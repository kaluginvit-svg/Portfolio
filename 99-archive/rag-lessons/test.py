def _pinecone_client() -> tuple[PineconeVectorClient, str]:
    """Тот же выбор индекса, что в bot.py — чтобы заливка не уходила в другой индекс."""
    host = os.getenv("PINECONE_INDEX_HOST", "").strip()
    idx_name = os.getenv("PINECONE_INDEX_NAME", "").strip()
    if host:
        return PineconeVectorClient(index_host=host), host
    if idx_name:
        return PineconeVectorClient(index_name=idx_name), idx_name
    return PineconeVectorClient(index_name="nemo"), "nemo"


