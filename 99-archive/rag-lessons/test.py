def get_embedding(text: str, *, target_dim: int | None = None) -> list[float]:
    """Один текст → один вектор (ProxyAPI / OpenAI embeddings)."""
    if target_dim is not None and "text-embedding-3" in _embedding_model:
        r = client.embeddings.create(input=text, model=_embedding_model, dimensions=target_dim)
    else:
        r = client.embeddings.create(input=text, model=_embedding_model)
    return list(r.data[0].embedding)


def _meta_for_row(row_id: int, category: str, text: str) -> dict[str, str | int]:
    t = text[:4000]
    c = category[:4000]
    return {
        "row_id": row_id,
        "category": c,
        "text": t,
        "phrase": t,  # ленивый бот тянет phrase или text — дублируем
    }


def main() -> None:
    if len(_CORPUS_ROWS) != _EXPECTED:
        sys.exit(f"Ожидалось {_EXPECTED} строк, сейчас {len(_CORPUS_ROWS)}.")

    skip_pc = os.getenv("TEST_SKIP_PINECONE", "").lower() in ("1", "true", "yes")
    if skip_pc:
        _id, _cat, _txt = _CORPUS_ROWS[0]
        v = get_embedding(_txt, target_dim=None)
        print("TEST_SKIP_PINECONE=1 — только get_embedding первой строки, len=", len(v))
        return

    pc, index_label = _pinecone_client()
    dim = getattr(pc.check_index(), "dimension", None)

    for row_id, category, text in _CORPUS_ROWS:
        vec = get_embedding(text, target_dim=dim)
        if dim is not None and len(vec) != dim:
            sys.exit(f"Строка id={row_id}: длина вектора {len(vec)} != dimension индекса {dim}.")
        rec: PineVectorRecord = {
            "id": str(row_id),
            "values": vec,
            "metadata": _meta_for_row(row_id, category, text),
        }
        pc.upsert_vectors([rec], show_progress=False)

    stats = pc.check_index()
    print(
        f"Индекс {index_label!r}: залито {len(_CORPUS_ROWS)} векторов, "
        f"total_vector_count={getattr(stats, 'total_vector_count', '?')}"
    )


if __name__ == "__main__":
    main()
