# -*- coding: utf-8 -*-
"""
Скрипт выполняет SQL-запросы домашнего задания по данным о странах.
Читает CSV, загружает в SQLite, выполняет запросы и выводит результаты в файл.
"""
import sqlite3
import csv

# Путь к файлу базы данных SQLite (создаётся/перезаписывается при запуске)
DB_PATH = "homework_countries.db"
# Путь к CSV-файлу с данными о странах (колонки: Страна, Население, Регион)
CSV_PATH = "countries.csv"


def main(output=None):
    # Если поток вывода не передан — выводим в консоль (stdout)
    if output is None:
        output = __import__("sys").stdout

    # Функция печати в заданный поток (файл или консоль)
    def prn(*a, **k):
        print(*a, **k, file=output)

    # Подключаемся к базе SQLite (файл создаётся автоматически, если его нет)
    conn = sqlite3.connect(DB_PATH)

    # Удаляем таблицу countries, если она уже есть (чтобы каждый запуск начинался с чистой таблицы)
    conn.execute("DROP TABLE IF EXISTS countries")

    # Создаём таблицу countries с тремя колонками: Страна (текст), Население (целое число), Регион (текст)
    conn.execute("""
        CREATE TABLE countries (
            Страна TEXT,
            Население INTEGER,
            Регион TEXT
        )
    """)

    # Открываем CSV-файл в кодировке UTF-8 для корректного чтения кириллицы
    with open(CSV_PATH, "r", encoding="utf-8") as f:
        # DictReader читает первую строку как заголовки и каждую строку возвращает как словарь
        reader = csv.DictReader(f)
        for row in reader:
            # Берём значение колонки «Страна»; пустую строку превращаем в NULL (None)
            country = row.get("Страна") or None
            # Берём «Население», убираем пробелы; если пусто — считаем 0
            pop = row.get("Население", "0").strip()
            pop = int(pop) if pop else 0
            # Берём «Регион»; пустое — как None
            region = row.get("Регион") or None
            # Вставляем одну строку в таблицу (?) — плейсхолдеры защищают от SQL-инъекций
            conn.execute(
                "INSERT INTO countries (Страна, Население, Регион) VALUES (?, ?, ?)",
                (country, pop, region),
            )

    # Сохраняем все изменения в базе (без commit данные не записываются на диск)
    conn.commit()

    # Создаём курсор для выполнения SQL-запросов и получения результатов
    cur = conn.cursor()

    # Список заданий: (описание, комментарий для txt, текст SQL-запроса)
    tasks = [
        ("1. Таблица по населению (убывание)",
         "Действие: сортировка таблицы по столбцу «Население» в порядке убывания (ORDER BY Население DESC).",
         "SELECT * FROM countries ORDER BY Население DESC"),
        ("2. Первые 5 строк отсортированной таблицы",
         "Действие: вывод первых 5 строк таблицы, отсортированной по населению (LIMIT 5).",
         "SELECT * FROM countries ORDER BY Население DESC LIMIT 5"),
        ("3. Только страны Европы",
         "Действие: фильтр по региону — только страны, где Регион = 'Европа' (WHERE).",
         "SELECT * FROM countries WHERE Регион = 'Европа'"),
        ("4. Строки, где Страна IS NOT NULL",
         "Действие: оставляем только строки, где в столбце «Страна» значение не NULL.",
         "SELECT * FROM countries WHERE Страна IS NOT NULL"),
        ("5. Население > 150 млн (отсортировано)",
         "Действие: фильтр WHERE по населению > 150 млн, сортировка по убыванию населения.",
         "SELECT * FROM countries WHERE Население > 150 ORDER BY Население DESC"),
        ("6. Население от 50 до 100 млн (отсортировано)",
         "Действие: фильтр WHERE — население в диапазоне 50–100 млн (BETWEEN), сортировка по убыванию.",
         "SELECT * FROM countries WHERE Население BETWEEN 50 AND 100 ORDER BY Население DESC"),
        ("7. Страны на гласную (А,Е,И,О,У,Э,Ю,Я)",
         "Действие: фильтр по странам, название которых начинается с гласной (LIKE 'А%' и т.д.).",
         """SELECT * FROM countries 
            WHERE Страна IS NOT NULL 
              AND (Страна LIKE 'А%' OR Страна LIKE 'Е%' OR Страна LIKE 'И%' 
                OR Страна LIKE 'О%' OR Страна LIKE 'У%' OR Страна LIKE 'Э%' 
                OR Страна LIKE 'Ю%' OR Страна LIKE 'Я%')
            ORDER BY Население DESC"""),
    ]

    # Заголовок отчёта в начале файла (комментарий о содержании)
    prn("ДОМАШНЕЕ ЗАДАНИЕ: РЕЗУЛЬТАТЫ SQL-ЗАПРОСОВ ПО ДАННЫМ О СТРАНАХ")
    prn("Источник данных: countries.csv → таблица countries(Страна, Население, Регион).")
    prn("Население указано в миллионах человек.")
    prn("")

    # Для каждого задания выполняем запрос и выводим результат в виде таблицы
    for title, comment, sql in tasks:
        # Разделитель и заголовок блока
        prn("\n" + "=" * 60)
        prn(title)
        prn("=" * 60)
        # Поясняющий комментарий для txt (что делаем в этом задании)
        prn("Комментарий:", comment)
        # Показываем начало текста SQL-запроса (до 80 символов)
        prn("SQL:", sql.strip()[:80] + ("..." if len(sql) > 80 else ""))
        # Выполняем запрос в базе
        cur.execute(sql)
        # Получаем все строки результата
        rows = cur.fetchall()
        # Названия колонок берём из описания курсора (cur.description)
        cols = [d[0] for d in cur.description]
        # Вычисляем минимальную ширину колонки по названию (не меньше 4 символов)
        lens = [max(len(str(c)), 4) for c in cols]
        # Проходим по строкам и подбираем ширину под самое длинное значение в каждой колонке
        for r in rows:
            for i, v in enumerate(r):
                lens[i] = max(lens[i], len(str(v)) if v is not None else 4)
        # Собираем формат вывода: выравнивание влево с учётом ширины каждой колонки
        fmt = "  ".join("%-" + str(l) + "s" for l in lens)
        # Печатаем заголовок таблицы (названия колонок)
        prn(fmt % tuple(cols))
        # Печатаем линию под заголовком
        prn("-" * (sum(lens) + 2 * (len(cols) - 1)))
        # Печатаем каждую строку результата; NULL заменяем на пустую строку
        for r in rows:
            prn(fmt % tuple(str(x) if x is not None else "" for x in r))
        # Итоговое количество строк в результате
        prn("Строк:", len(rows))

    # Закрываем соединение с базой (освобождаем ресурсы и снимаем блокировки с файла)
    conn.close()
    prn("\nГотово.")


# При запуске файла как основной программы (не при импорте)
if __name__ == "__main__":
    import sys
    # Открываем файл для записи результатов в UTF-8 (чтобы кириллица отображалась корректно)
    with open("homework_results.txt", "w", encoding="utf-8") as f:
        # Передаём файл в main — весь вывод пойдёт в homework_results.txt
        main(f)
    # Сообщаем пользователю, куда сохранены результаты
    print("Результаты сохранены в homework_results.txt")
